                    (anodes(in+1,iv)-anodes(in,iv))
	enddo FNODES

	endif

!	print*,iv,rva(iv)
	enddo VSET
!---------------------

	 ibase = floor(rva)
	 where( ibase < 1) ibase=1

	 itop= ceiling(rva)
	 where( itop > nva) itop=nva

	wb=1.0-(rva-ibase)
