real function mean_tau(clrtau1)
USE FUINPUT     ,only : fi,mccx,mxovl
implicit none
real x , y ,z,scf,cf,clrtau,clrtau1
integer i
 
z=0
do i = 1,mccx
 cf = fi%fc(i)%cldfrac
if ( cf <= 0 ) cycle

scf= scf+ cf
x = sum(fi%fc(i)%tau_vis(1:mxovl))
y=-5
if ( x > 0 ) y = log( x)

z= z +  cf*y 
!print'(i4,2f10.3,3f10.4)', i,cf ,scf,x,y,z

enddo

 clrtau= -5
 if( clrtau1 > 0.007 ) clrtau=log(clrtau1)
 

z = z +  clrtau *(1.0-scf)
mean_tau = exp(z) 
!print*,mean_tau
end  function mean_tau

