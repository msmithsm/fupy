        subroutine wpar_lut
        real swx , wx 
        real rmin,rmax
        integer i,j ,k , iv ,in
        integer  ivax(nv,2**nv)
        real ,dimension(nv) :: wb,wt,rva
        integer,dimension(nv) :: ibase,itop     
!----------------------------------------
        if ( inital ) call tf_table(nv,2**nv,ichoose);inital = .false.
        val(1:nv)=anodes(1,1:nv)

        val(1)=  wpar%u0
        val(2)=  log(wpar%tau)
        val(3)=  wpar%salb

!	print*,val
         VSET : do iv=1,nv 
        rmin = anodes(1,iv)
        rmax = anodes(nva(iv),iv)

        if (val(iv) <= rmin )then 
          rva(iv) = 1
        elseif (val(iv) >= rmax ) then
          rva(iv) = nva(iv)
        else

        FNODES : do in = 1,nva(iv)-1
        if( val(iv) >= anodes(in,iv) .and. val(iv) < anodes(in+1,iv) )  &
        rva(iv)= in+( val(iv)-anodes(in,iv)) / &
                    (anodes(in+1,iv)-anodes(in,iv))
        enddo FNODES

        endif

!       print*,iv,rva(iv)
        enddo VSET
!---------------------

         ibase = floor(rva)
         where( ibase < 1) ibase=1

         itop= ceiling(rva)
         where( itop > nva) itop=nva

        wb=1.0-(rva-ibase)
        wt=1.0-wb
        
        swx=0
        
        stab=0

        NODE : do j=1,2**nv
        wx=1.0
	
         DIMENSION: do i=1,nv

         if( ichoose(i,j) ) then
          ivax(i,j) = itop(i)
          wx=wx*wt(i)
         else
          ivax(i,j) = ibase(i)
          wx=wx*wb(i)
         endif

         enddo DIMENSION

         if( wx > 1.0E-10 ) then
       
         k = ilui(nva,nv,ivax(1,j))

         swx       = swx  +     wx

         stab(1:12) =   stab(1:12) +    table(k,1:12)    * wx 

!          print'(3I6,2I6,2f10.4,4f10.3)',ivax(1:nv,j),j,k, wx,swx, table(k,1:4)
         if (swx >= .9999)exit
        endif

        enddo NODE

        if ( swx .ne. 1.0000 .and. swx .ne. 0.0) stab =stab /swx !!! Incase of partial weightings

!       print'(67x,2(7f7.4,1x))' ,stab
		
!	where ( stab == 0 ) stab=1.0

	wpar%r7(1)    = stab(1)
	wpar%r10(1)   = stab(2)
 	wpar%ract(1)  = stab(3)
	wpar%rchl(1)  = stab(4)



	wpar%r7(2)    = stab(5)
	wpar%r10(2)   = stab(6)
 	wpar%ract(2)  = stab(7)
	wpar%rchl(2)  = stab(8)
	
	
	wpar%r7(3)    = stab(9)
	wpar%r10(3)   = stab(10)
 	wpar%ract(3)  = stab(11)
	wpar%rchl(3)  = stab(12)


        return
        end subroutine  wpar_lut
!-----------------------------------------------------------------------
        integer function ilui(nva,nv,iva)
        implicit none
        integer nv,i,k
        integer  nva(nv),iva(nv)
        integer  mva(nv)

        mva(1)=nva(1)
        do i=2,nv
         mva(i)=mva(i-1)*nva(i)
        enddo

        k= iva(1)
        do i=2,nv
        k= k+ (iva(i)-1)*mva(i-1)
        enddo

        ilui=k
        return
        end function ilui
