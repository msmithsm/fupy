	val(2) =  acos(uvco%u0)*(180.0/3.14159)	! Cos Sol to SZA
	val(3) =  uvco%cldtau       !! Cloudtau
	
	 call tf_table(nv,2**nv,ichoose)
!--------------------
	 VSET : do iv=1,nv 
	rmin = anodes(1,iv)
	rmax = anodes(nva(iv),iv)

	if (val(iv) <= rmin )then 
	  rva(iv) = 1
	elseif (val(iv) >= rmax ) then
	  rva(iv) = nva(iv)
	else

